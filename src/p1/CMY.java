package p1;

public class CMY {
private int C;
private int M;
private int Y;
public CMY() {
	super();
}
public CMY(int c, int m, int y) {
	super();
	C = c;
	M = m;
	Y = y;
}
public int getC() {
	return C;
}
public void setC(int c) {
	C = c;
}
public int getM() {
	return M;
}
public void setM(int m) {
	M = m;
}
public int getY() {
	return Y;
}
public void setY(int y) {
	Y = y;
}


}

