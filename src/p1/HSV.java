package p1;

public class HSV {
private int H ;
private float S;
private float V;



public HSV() {
	super();
}
public HSV(int h, float s, float v) {
	super();
	H = h;
	S = s;
	V = v;
}
public int getH() {
	return H;
}
public void setH(int h) {
	H = h;
}
public float getS() {
	return S;
}
public void setS(float s) {
	S = s;
}
public float getV() {
	return V;
}
public void setV(float v) {
	V = v;
}


}
